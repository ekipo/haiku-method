#!/usr/bin/env node
/**
 * Build haiku-ui (the SPA) and embed the output as a TypeScript string
 * constant consumed by the MCP HTTP server.
 *
 * 1. npm run build -w haiku-ui (or equivalent; runs vite build in the
 *    sibling package).
 * 2. Read packages/haiku-ui/dist/index.html and inline all CSS/JS assets
 *    into it.
 * 3. Write to packages/haiku/src/haiku-ui-html.ts as:
 *       export const HAIKU_UI_HTML: string = `...`
 *    Also gzip the output and assert size <= budget (packages/haiku-ui/budget.json).
 */
import { execSync } from "node:child_process"
import { existsSync, readFileSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"
import { gzipSync } from "node:zlib"

const __dir = dirname(fileURLToPath(import.meta.url))
const root = join(__dir, "..")
const repoRoot = join(root, "..", "..")
const haikuUiDir = join(root, "..", "haiku-ui")
const distDir = join(haikuUiDir, "dist")
const distHtml = join(distDir, "index.html")
const tsFile = join(root, "src", "haiku-ui-html.ts")
const budgetFile = join(haikuUiDir, "budget.json")

// Install deps if needed
if (!existsSync(join(repoRoot, "node_modules", "vite"))) {
	console.error("Installing workspace dependencies...")
	execSync("npm install", { cwd: repoRoot, stdio: "inherit" })
}

// Build the haiku-ui SPA via the workspace script
console.error("Building haiku-ui SPA...")
try {
	execSync("npm run build -w haiku-ui", { cwd: repoRoot, stdio: "inherit" })
} catch {
	// Fallback for environments that prefer bun
	execSync("bun run build", { cwd: haikuUiDir, stdio: "inherit" })
}

// Read the built HTML
if (!existsSync(distHtml)) {
	console.error(`ERROR: Expected ${distHtml} to exist after build`)
	process.exit(1)
}

let html = readFileSync(distHtml, "utf-8")

// Inline all <script> and <link rel="stylesheet"> assets
// Replace <script type="module" crossorigin src="/assets/xxx.js"></script>
html = html.replace(
	/<script\b[^>]*\bsrc="\/assets\/([^"]+)"[^>]*><\/script>/g,
	(match, filename) => {
		const filePath = join(distDir, "assets", filename)
		if (!existsSync(filePath)) {
			console.error(`WARNING: Referenced asset not found: ${filePath}`)
			return match
		}
		const content = readFileSync(filePath, "utf-8")
		return `<script type="module">${content}</script>`
	},
)

// Replace <link rel="stylesheet" crossorigin href="/assets/xxx.css">
html = html.replace(
	/<link\b[^>]*\bhref="\/assets\/([^"]+\.css)"[^>]*>/g,
	(match, filename) => {
		const filePath = join(distDir, "assets", filename)
		if (!existsSync(filePath)) {
			console.error(`WARNING: Referenced CSS asset not found: ${filePath}`)
			return match
		}
		const content = readFileSync(filePath, "utf-8")
		return `<style>${content}</style>`
	},
)

// Bundle-size budget check
const gzipBytes = gzipSync(html).length
let budget = { bundleGzipMaxBytes: 1048576, lastKnownGzipBytes: 0 }
if (existsSync(budgetFile)) {
	try {
		budget = JSON.parse(readFileSync(budgetFile, "utf-8"))
	} catch {
		// fall through
	}
}

if (gzipBytes > budget.bundleGzipMaxBytes) {
	console.error(
		`ERROR: haiku-ui bundle gzip size ${gzipBytes} bytes exceeds budget ${budget.bundleGzipMaxBytes} bytes`,
	)
	process.exit(1)
}

// Record the measured size back into budget.json (non-fatal if write fails)
try {
	const updated = { ...budget, lastKnownGzipBytes: gzipBytes }
	writeFileSync(budgetFile, `${JSON.stringify(updated, null, 2)}\n`)
} catch (err) {
	console.error(`WARNING: Could not update ${budgetFile}: ${err}`)
}

// Write it as a TypeScript string export
const tsContent = `// AUTO-GENERATED by scripts/bundle-haiku-ui.mjs — do not edit manually.\nexport const HAIKU_UI_HTML: string = ${JSON.stringify(html)};\n`
writeFileSync(tsFile, tsContent)

const bytes = Buffer.byteLength(html, "utf-8")
console.error(
	`haiku-ui (inlined): ${bytes} bytes raw, ${gzipBytes} bytes gzipped -> ${tsFile}`,
)
